exports.rftfsub = function (n, aBuffer, nc, cBuffer, cOffset) {
	let j, k, kk;
	let wkr, wki, xr, xi, yr, yi;

	const a = new Float64Array(aBuffer);
	const c = new Float64Array(cBuffer).subarray(cOffset);

	const m = n >> 1;
	const ks = 2 * nc / m;
	kk = 0;
	for (j = 2; j < m; j += 2) {
		k = n - j;
		kk += ks;
		wkr = 0.5 - c[nc - kk];
		wki = c[kk];
		xr = a[j] - a[k];
		xi = a[j + 1] + a[k + 1];
		yr = wkr * xr - wki * xi;
		yi = wkr * xi + wki * xr;
		a[j] -= yr;
		a[j + 1] -= yi;
		a[k] += yr;
		a[k + 1] -= yi;
	}
};

exports.rftbsub = function (n, aBuffer, nc, cBuffer, cOffset) {
	let j, k, kk;
	let wkr, wki, xr, xi, yr, yi;

	const a = new Float64Array(aBuffer);
	const c = new Float64Array(cBuffer).subarray(cOffset);

	a[1] = -a[1];
	const m = n >> 1;
	const ks = 2 * nc / m;
	kk = 0;
	for (j = 2; j < m; j += 2) {
		k = n - j;
		kk += ks;
		wkr = 0.5 - c[nc - kk];
		wki = c[kk];
		xr = a[j] - a[k];
		xi = a[j + 1] + a[k + 1];
		yr = wkr * xr + wki * xi;
		yi = wkr * xi - wki * xr;
		a[j] -= yr;
		a[j + 1] = yi - a[j + 1];
		a[k] += yr;
		a[k + 1] = yi - a[k + 1];
	}
	a[m + 1] = -a[m + 1];
};
